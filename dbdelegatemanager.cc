/* ------------------------------------------------------------------------- */
bool DbDelegateManager::init()
{
    bool b_ret = false;
    for (;;) {
        if (uniq_ != NULL) {
            b_ret = true;
            break;
        }
        uniq_ = new DbDelegateManager ();


        b_ret = true;
        break;
    }
    return b_ret;
}
/* ========================================================================= */

/* ------------------------------------------------------------------------- */
void DbDelegateManager::end ()
{
    for (;;) {
        if (uniq_ == NULL) {
            break;
        }


        delete uniq_;
        break;
    }
}
